const modal =
document.getElementById("galleryModal");

const galleryImage =
document.getElementById("galleryImage");

const galleryTitle =
document.getElementById("galleryTitle");

const imageCounter =
document.getElementById("imageCounter");

const loopNotice =
document.getElementById("loopNotice");

const autoPlayBtn =
document.getElementById("autoPlayBtn");

const searchInput =
document.getElementById("searchInput");

// =====================================
// VARIABLES
// =====================================

let currentGallery = null;
let currentIndex = 0;

let autoSlide = null;
let autoPlayRunning = false;

// =====================================
// SHOW IMAGE
// =====================================

function showImage() {

    galleryImage.style.opacity = "0";

    setTimeout(() => {

        galleryImage.src =
        currentGallery.images[currentIndex];

        galleryImage.style.opacity = "1";

        imageCounter.textContent =
        `${currentIndex + 1} / ${currentGallery.images.length}`;

    }, 180);

    localStorage.setItem("savedIndex", currentIndex);

}

// =====================================
// LOOP NOTICE
// =====================================

function showLoopNotice() {

    loopNotice.style.display =
    "block";

    setTimeout(() => {

        loopNotice.style.display =
        "none";

    }, 1800);

}

// =====================================
// NEXT
// =====================================

function nextImage() {

    currentIndex++;

    if (
        currentIndex >=
        currentGallery.images.length
    ) {

        currentIndex = 0;

        showLoopNotice();

    }

    showImage();

}

// =====================================
// PREVIOUS
// =====================================

function previousImage() {

    currentIndex--;

    if (
        currentIndex < 0
    ) {

        currentIndex =
        currentGallery.images.length - 1;

        showLoopNotice();

    }

    showImage();

}

// =====================================
// OPEN GALLERY
// =====================================

document
.querySelectorAll(".gallery-card")
.forEach(card => {

    card.addEventListener(
        "click",
        () => {

            const key =
            card.dataset.gallery;

            currentGallery =
            galleries[key];

            currentIndex = 0;

            localStorage.setItem("savedGallery", key);
            localStorage.setItem("savedindex", 0);

            galleryTitle.textContent =
            currentGallery.title;

            modal.style.display =
            "flex";

            showImage();

        }
    );

});

// =====================================
// BUTTONS
// =====================================

document
.getElementById("nextBtn")
.addEventListener(
    "click",
    nextImage
);

document
.getElementById("prevBtn")
.addEventListener(
    "click",
    previousImage
);

// =====================================
// CLOSE
// =====================================

function closeGallery() {

    stopAutoSlide();

    modal.style.display =
    "none";

    localStorage.removeItem("savedGallery");
    localStorage.removeItem("savedIndex");

}

document
.getElementById("closeBtn")
.addEventListener(
    "click",
    closeGallery
);

document
.getElementById("backBtn")
.addEventListener(
    "click",
    closeGallery
);

// =====================================
// AUTO PLAY
// =====================================

function startAutoSlide() {

    autoSlide =
    setInterval(
        nextImage,
        3000
    );

    autoPlayRunning = true;

    autoPlayBtn.textContent =
    "⏸ Stop Auto";

}

function stopAutoSlide() {

    clearInterval(autoSlide);

    autoPlayRunning = false;

    autoPlayBtn.textContent =
    "▶ Auto Slide";

}

autoPlayBtn.addEventListener(
    "click",
    () => {

        if (
            autoPlayRunning
        ) {

            stopAutoSlide();

        } else {

            startAutoSlide();

        }

    }
);

// =====================================
// SEARCH
// =====================================

searchInput.addEventListener(
    "input",
    function () {

        const value =
        this.value
        .toLowerCase()
        .trim();

        document
        .querySelectorAll(
            ".gallery-card"
        )
        .forEach(card => {

            const text =
            card.innerText
            .toLowerCase();

            if (
                text.includes(value)
            ) {

                card.style.display =
                "block";

            } else {

                card.style.display =
                "none";

            }

        });

    }
);

// =====================================
// SWIPE SUPPORT
// =====================================

let touchStartX = 0;

galleryImage.addEventListener(
    "touchstart",
    e => {

        touchStartX =
        e.touches[0].clientX;

    }
);

galleryImage.addEventListener(
    "touchend",
    e => {

        let touchEndX =
        e.changedTouches[0].clientX;

        let diff =
        touchStartX -
        touchEndX;

        if (diff > 50) {

            nextImage();

        }

        if (diff < -50) {

            previousImage();

        }

    }
);

// =====================================
// KEYBOARD SUPPORT
// =====================================

document.addEventListener(
    "keydown",
    e => {

        if (
            modal.style.display !==
            "flex"
        ) return;

        if (
            e.key ===
            "ArrowRight"
        ) {

            nextImage();

        }

        if (
            e.key ===
            "ArrowLeft"
        ) {

            previousImage();

        }

        if (
            e.key ===
            "Escape"
        ) {

            closeGallery();

        }

    }
);

window.addEventListener(
    "load",
    () => {
        const savedGallery = 
        localStorage.getItem(
            "savedGallery"
        );
        const savedIndex = 
        parseInt(
            localStorage.getItem(
                "savedIndex"
            )
        );
        if(savedGallery && galleries[savedGallery]){
            currentIndex = isNan(savedIndex) ? 0: savedIndex;
            galleryTitle.textContent = currentGallery.tittle;
            modal.style.display = "flex";
            showImage();
        }
    }
);